package Test;

public class LoopConcepts {

	public static void main(String[] args) {

		// 1. while loop
		int i = 0;// initialization
		while (i <= 10) {// conditon
			System.out.println(i);
			i++;// increment/decrement
		}

		// 2. for loop
		
		for(int j=10;j>=0;j--)
		{
			System.out.println(j);
		}

	}

}
